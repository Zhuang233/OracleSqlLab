DROP TABLE STUDENT;
DROP TABLE COACH;
DROP TABLE DEPT;

-- 学生表（学号，姓名，性别，年龄，学院号，教练号，年级）
CREATE TABLE STUDENT (
    STUDENTNO INT,
    NAME VARCHAR(50),
    GENDER VARCHAR(3),
    AGE INT,
    DEPTNO VARCHAR(10),
    COACHNO INT,
    GRADE INT,
    CONSTRAINT PK_STUDENT PRIMARY KEY(STUDENTNO)
);

--教练表（教练号，工龄，教练名）
CREATE TABLE COACH (
    COACHNO INT,
    WORKAGE INT,
    NAME VARCHAR(10),
    CONSTRAINT PK_COACH PRIMARY KEY(COACHNO)
);

--学院表（学院号，学院名，学院人数）
CREATE TABLE DEPT (
    DEPTNO INT,
    NAME VARCHAR(20),
    NNT INT,
    CONSTRAINT PK_DEPT PRIMARY KEY(DEPTNO)
);

